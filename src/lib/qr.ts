/**
 * Encode text as a QR symbol (byte mode, EC level M, version 1–10).
 * Throws when the UTF-8 payload is empty or exceeds QR_MAX_BYTES.
 */
export function encodeQr(text: string, options?: QrOptions): QrMatrix {
  const bytes = new TextEncoder().encode(text);
  if (bytes.length === 0) {
    throw new Error("QR payload must not be empty");
  }
  const versionIndex = VERSIONS.findIndex(
    (_, index) => bytes.length <= byteCapacity(index),
  );
  if (versionIndex === -1) {
    throw new Error(
      `QR payload of ${bytes.length} bytes exceeds the ${QR_MAX_BYTES}-byte capacity`,
    );
  }
  const version = versionIndex + 1;
  const size = 17 + 4 * version;

  const grid: Grid = {
    size,
    modules: Array.from({ length: size }, () => new Array<boolean>(size).fill(false)),
    isFunction: Array.from({ length: size }, () => new Array<boolean>(size).fill(false)),
  };
  drawFunctionPatterns(grid, version);
  placeData(grid, buildCodewords(bytes, versionIndex));

  const applyMask = (mask: number): boolean[][] => {
    const masked = grid.modules.map((row) => row.slice());
    for (let r = 0; r < size; r++) {
      for (let c = 0; c < size; c++) {
        if (!grid.isFunction[r][c] && maskBit(mask, r, c)) {
          masked[r][c] = !masked[r][c];
        }
      }
    }
    const view: Grid = { size, modules: masked, isFunction: grid.isFunction };
    drawFormatBits(view, mask);
    return masked;
  };

  let mask: number;
  if (options?.mask !== undefined) {
    if (!Number.isInteger(options.mask) || options.mask < 0 || options.mask > 7) {
      throw new Error(`Invalid mask pattern: ${options.mask}`);
    }
    mask = options.mask;
  } else {
    mask = 0;
    let best = Infinity;
    for (let candidate = 0; candidate < 8; candidate++) {
      const score = penaltyScore(applyMask(candidate));
      if (score < best) {
        best = score;
        mask = candidate;
      }
    }
  }

  return { version, mask, size, modules: applyMask(mask) };
}

export type QrDisplay =
  | { status: "pending" }
  | { status: "ready"; qr: QrMatrix }
  | { status: "unavailable" };

/**
 * Display state for a QR code of `url` (fix round 1, MAJOR-2): `pending`
 * while the URL is not yet known on the client, `ready` with the matrix, or
 * an explicit `unavailable` when the URL exceeds capacity — the page then
 * shows an honest message instead of an endless skeleton.
 */
export function qrDisplayFor(url: string | null): QrDisplay {
  if (url === null) return { status: "pending" };
  try {
    return { status: "ready", qr: encodeQr(url) };
  } catch {
    return { status: "unavailable" };
  }
}

/**
 * SVG path data drawing every dark module as a 1x1 square, for use in
 * `<path d={...} />` inside `<svg viewBox="0 0 size size">` (add a quiet
 * zone of 4 modules around it at the embedding site).
 */
export function qrSvgPath(qr: QrMatrix): string {
  const parts: string[] = [];
  for (let r = 0; r < qr.size; r++) {
    for (let c = 0; c < qr.size; c++) {
      if (qr.modules[r][c]) parts.push(`M${c} ${r}h1v1h-1z`);
    }
  }
  return parts.join("");
}
