// Minimal QR code encoder — byte mode, error correction level M,
// versions 1–10 (auto-selected by payload size, up to 213 UTF-8 bytes).
//
// Hand-written for T15 / INS-20 so the /install page can render its QR code
// fully offline with zero runtime dependencies (constitution C1 + C4 / rule
// 3: no new libraries). Implements the relevant subset of ISO/IEC 18004:
// GF(256) Reed-Solomon EC, (unequal) block interleaving, module placement,
// alignment-pattern grid, version information (v7+), all eight mask patterns
// with standard penalty scoring, and format information. Correctness is
// pinned in qr.test.ts against reference vectors generated by an independent
// conformant encoder (see qr.test-vectors.ts).
//
// 213 bytes covers any realistic install URL (long Vercel preview or custom
// hostnames included); beyond that encodeQr throws and the caller degrades
// honestly (see qrDisplayFor).

export interface QrMatrix {
  /** QR version, 1–10. */
  version: number;
  /** Mask pattern actually applied, 0–7. */
  mask: number;
  /** Modules per side (17 + 4 x version). */
  size: number;
  /** modules[row][col] — true = dark. */
  modules: boolean[][];
}

export interface QrOptions {
  /** Force a mask pattern (0–7) instead of penalty-based selection. */
  mask?: number;
}

// Per version (index 0 = v1), EC level M, byte mode: data codewords per RS
// block (shorter blocks first, per the standard) and EC codewords per block.
const VERSIONS = [
  { blocks: [16], ecPerBlock: 10 },
  { blocks: [28], ecPerBlock: 16 },
  { blocks: [44], ecPerBlock: 26 },
  { blocks: [32, 32], ecPerBlock: 18 },
  { blocks: [43, 43], ecPerBlock: 24 },
  { blocks: [27, 27, 27, 27], ecPerBlock: 16 },
  { blocks: [31, 31, 31, 31], ecPerBlock: 18 },
  { blocks: [38, 38, 39, 39], ecPerBlock: 22 },
  { blocks: [36, 36, 36, 37, 37], ecPerBlock: 22 },
  { blocks: [43, 43, 43, 43, 44], ecPerBlock: 26 },
] as const;

// Byte-mode character-count field width: 8 bits for v1–9, 16 bits for v10+.
function lengthBits(version: number): number {
  return version >= 10 ? 16 : 8;
}

function dataCodewordCount(versionIndex: number): number {
  return VERSIONS[versionIndex].blocks.reduce((sum, n) => sum + n, 0);
}

// Payload capacity in bytes: 8*D >= 4 (mode) + length field + 8*n.
function byteCapacity(versionIndex: number): number {
  return Math.floor(
    (8 * dataCodewordCount(versionIndex) - 4 - lengthBits(versionIndex + 1)) /
      8,
  );
}

/** Maximum payload in UTF-8 bytes (version 10, EC level M, byte mode). */
export const QR_MAX_BYTES = 213;

// ---------------------------------------------------------------------------
// GF(256) arithmetic (primitive polynomial 0x11d) and Reed-Solomon EC.
// ---------------------------------------------------------------------------

const GF_EXP = new Uint8Array(512);
const GF_LOG = new Uint8Array(256);
{
  let x = 1;
  for (let i = 0; i < 255; i++) {
    GF_EXP[i] = x;
    GF_LOG[x] = i;
    x <<= 1;
    if (x & 0x100) x ^= 0x11d;
  }
  for (let i = 255; i < 512; i++) GF_EXP[i] = GF_EXP[i - 255];
}

function gfMul(a: number, b: number): number {
  if (a === 0 || b === 0) return 0;
  return GF_EXP[GF_LOG[a] + GF_LOG[b]];
}

/**
 * Reed-Solomon error-correction codewords: remainder of
 * data(x) * x^ecCount modulo the generator polynomial of degree ecCount.
 */
export function rsEcCodewords(data: number[], ecCount: number): number[] {
  // Generator polynomial: product of (x - alpha^i) for i = 0..ecCount-1.
  let gen = [1];
  for (let i = 0; i < ecCount; i++) {
    const next = new Array<number>(gen.length + 1).fill(0);
    for (let j = 0; j < gen.length; j++) {
      next[j] ^= gfMul(gen[j], GF_EXP[i]);
      next[j + 1] ^= gen[j];
    }
    gen = next;
  }
  // gen is little-endian (index = power of x); polynomial division wants the
  // leading coefficient first.
  gen.reverse();

  const remainder = new Array<number>(ecCount).fill(0);
  for (const byte of data) {
    const factor = byte ^ remainder[0];
    remainder.shift();
    remainder.push(0);
    for (let i = 0; i < ecCount; i++) {
      remainder[i] ^= gfMul(gen[i + 1], factor);
    }
  }
  return remainder;
}

// ---------------------------------------------------------------------------
// Data encoding: byte mode segment -> padded codewords -> interleaved blocks.
// ---------------------------------------------------------------------------

function buildCodewords(bytes: Uint8Array, versionIndex: number): number[] {
  const { blocks, ecPerBlock } = VERSIONS[versionIndex];
  const totalData = dataCodewordCount(versionIndex);

  // Bit stream: mode 0100, length field, payload, terminator, byte
  // alignment, pad codewords 0xec / 0x11.
  const bits: number[] = [];
  const pushBits = (value: number, count: number) => {
    for (let i = count - 1; i >= 0; i--) bits.push((value >> i) & 1);
  };
  pushBits(0b0100, 4);
  pushBits(bytes.length, lengthBits(versionIndex + 1));
  for (const b of bytes) pushBits(b, 8);
  const capacityBits = totalData * 8;
  pushBits(0, Math.min(4, capacityBits - bits.length)); // terminator
  while (bits.length % 8 !== 0) bits.push(0);

  const data: number[] = [];
  for (let i = 0; i < bits.length; i += 8) {
    let byte = 0;
    for (let j = 0; j < 8; j++) byte = (byte << 1) | bits[i + j];
    data.push(byte);
  }
  for (let pad = 0xec; data.length < totalData; pad ^= 0xec ^ 0x11) {
    data.push(pad);
  }

  // Split into (possibly unequal) blocks, compute EC per block, interleave
  // column-wise; shorter blocks simply run out first during interleaving.
  const dataBlocks: number[][] = [];
  const ecBlocks: number[][] = [];
  let offset = 0;
  for (const blockSize of blocks) {
    const block = data.slice(offset, offset + blockSize);
    offset += blockSize;
    dataBlocks.push(block);
    ecBlocks.push(rsEcCodewords(block, ecPerBlock));
  }
  const out: number[] = [];
  const longestBlock = blocks[blocks.length - 1];
  for (let i = 0; i < longestBlock; i++) {
    for (const block of dataBlocks) {
      if (i < block.length) out.push(block[i]);
    }
  }
  for (let i = 0; i < ecPerBlock; i++) {
    for (const block of ecBlocks) out.push(block[i]);
  }
  return out;
}

// ---------------------------------------------------------------------------
// Matrix construction.
