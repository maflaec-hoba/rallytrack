interface Grid {
  size: number;
  modules: boolean[][];
  /** Function/reserved modules — excluded from data placement and masking. */
  isFunction: boolean[][];
}

function setFunction(grid: Grid, row: number, col: number, dark: boolean) {
  grid.modules[row][col] = dark;
  grid.isFunction[row][col] = true;
}

function drawFinder(grid: Grid, row: number, col: number) {
  // 7x7 finder plus its one-module light separator, clipped at the edges.
  for (let dr = -1; dr <= 7; dr++) {
    for (let dc = -1; dc <= 7; dc++) {
      const r = row + dr;
      const c = col + dc;
      if (r < 0 || c < 0 || r >= grid.size || c >= grid.size) continue;
      const dist = Math.max(Math.abs(dr - 3), Math.abs(dc - 3));
      setFunction(grid, r, c, dist !== 2 && dist !== 4);
    }
  }
}

function drawFunctionPatterns(grid: Grid, version: number) {
  const { size } = grid;

  // Timing patterns (finders overwrite their ends below).
  for (let i = 0; i < size; i++) {
    setFunction(grid, 6, i, i % 2 === 0);
    setFunction(grid, i, 6, i % 2 === 0);
  }

  drawFinder(grid, 0, 0);
  drawFinder(grid, 0, size - 7);
  drawFinder(grid, size - 7, 0);

  // Alignment patterns on the standard grid, skipping the three corners
  // occupied by finders.
  const positions = alignmentPositions(version, size);
  const last = positions.length - 1;
  for (let i = 0; i <= last; i++) {
    for (let j = 0; j <= last; j++) {
      if (
        (i === 0 && j === 0) ||
        (i === 0 && j === last) ||
        (i === last && j === 0)
      ) {
        continue;
      }
      for (let dr = -2; dr <= 2; dr++) {
        for (let dc = -2; dc <= 2; dc++) {
          const dist = Math.max(Math.abs(dr), Math.abs(dc));
          setFunction(grid, positions[i] + dr, positions[j] + dc, dist !== 1);
        }
      }
    }
  }

  drawVersionInfo(grid, version);

  // Reserve the format-information areas (real bits drawn per mask) and set
  // the always-dark module.
  for (let i = 0; i <= 8; i++) {
    if (i !== 6) {
      setFunction(grid, 8, i, false);
      setFunction(grid, i, 8, false);
    }
  }
  for (let i = 0; i < 8; i++) {
    setFunction(grid, size - 1 - i, 8, false);
    setFunction(grid, 8, size - 8 + i, false);
  }
  setFunction(grid, size - 8, 8, true);
}

// Alignment-pattern center coordinates: 6, then evenly stepped up to
// size - 7 (standard formula; v1 has none).
function alignmentPositions(version: number, size: number): number[] {
  if (version === 1) return [];
  const count = Math.floor(version / 7) + 2;
  const step =
    Math.floor((version * 4 + count * 2 + 1) / (count * 2 - 2)) * 2;
  const positions = new Array<number>(count);
  positions[0] = 6;
  for (let i = count - 1, pos = size - 7; i >= 1; i--, pos -= step) {
    positions[i] = pos;
  }
  return positions;
}

// Version information (v7+): 6-bit version + 12-bit BCH(18,6) remainder
// (generator 0x1f25), drawn into the 3x6 blocks beside the top-right and
// bottom-left finders.
function drawVersionInfo(grid: Grid, version: number) {
  if (version < 7) return;
  let rem = version;
  for (let i = 0; i < 12; i++) rem = (rem << 1) ^ ((rem >> 11) * 0x1f25);
  const bits = (version << 12) | rem;
  for (let i = 0; i < 18; i++) {
    const dark = ((bits >> i) & 1) === 1;
    const a = grid.size - 11 + (i % 3);
    const b = Math.floor(i / 3);
    setFunction(grid, b, a, dark); // top-right block
    setFunction(grid, a, b, dark); // bottom-left block
  }
}

function placeData(grid: Grid, codewords: number[]) {
  const { size } = grid;
  let bitIndex = 0;
  const totalBits = codewords.length * 8;
  for (let right = size - 1; right >= 1; right -= 2) {
    if (right === 6) right = 5; // the vertical timing column is skipped
    const upward = ((right + 1) & 2) === 0;
    for (let vert = 0; vert < size; vert++) {
      const row = upward ? size - 1 - vert : vert;
      for (let j = 0; j < 2; j++) {
        const col = right - j;
        if (grid.isFunction[row][col] || bitIndex >= totalBits) continue;
        grid.modules[row][col] =
          ((codewords[bitIndex >> 3] >> (7 - (bitIndex & 7))) & 1) === 1;
        bitIndex++;
        // Remainder modules (v2+ have 7) stay light and only get masked.
      }
    }
  }
}

function maskBit(mask: number, row: number, col: number): boolean {
  switch (mask) {
    case 0:
      return (row + col) % 2 === 0;
    case 1:
      return row % 2 === 0;
    case 2:
      return col % 3 === 0;
    case 3:
      return (row + col) % 3 === 0;
    case 4:
      return (Math.floor(row / 2) + Math.floor(col / 3)) % 2 === 0;
    case 5:
      return ((row * col) % 2) + ((row * col) % 3) === 0;
    case 6:
      return (((row * col) % 2) + ((row * col) % 3)) % 2 === 0;
    case 7:
      return (((row + col) % 2) + ((row * col) % 3)) % 2 === 0;
    default:
      throw new Error(`Invalid mask pattern: ${mask}`);
  }
}

function drawFormatBits(grid: Grid, mask: number) {
  // EC level M has format value 0b00; BCH(15,5) with generator 0x537,
  // XOR-ed with the fixed pattern 0x5412.
  const data = mask; // (M << 3) | mask, M = 0
  let rem = data;
  for (let i = 0; i < 10; i++) rem = (rem << 1) ^ ((rem >> 9) * 0x537);
  const bits = ((data << 10) | rem) ^ 0x5412;
  const bit = (i: number) => ((bits >> i) & 1) === 1;

  const { size } = grid;
  // First copy, around the top-left finder.
  for (let i = 0; i <= 5; i++) setFunction(grid, i, 8, bit(i));
  setFunction(grid, 7, 8, bit(6));
  setFunction(grid, 8, 8, bit(7));
  setFunction(grid, 8, 7, bit(8));
  for (let i = 9; i < 15; i++) setFunction(grid, 8, 14 - i, bit(i));
  // Second copy, split between the other two finders.
  for (let i = 0; i < 8; i++) setFunction(grid, 8, size - 1 - i, bit(i));
  for (let i = 8; i < 15; i++) setFunction(grid, size - 15 + i, 8, bit(i));
}

// Penalty scoring (ISO/IEC 18004 rules N1–N4). Where the standard leaves
// room for interpretation (N3 window, N4 rounding) this follows the same
// reading as the reference encoder the tests pin against, so auto mask
// selection is reproducible against the fixtures.
function penaltyScore(modules: boolean[][]): number {
  const size = modules.length;
  let score = 0;

  // Rule N1: runs of >= 5 same-colored modules in a row/column.
  const runPenalty = (line: (i: number) => boolean) => {
    let runColor = line(0);
    let runLength = 1;
    for (let i = 1; i < size; i++) {
      if (line(i) === runColor) {
        runLength++;
      } else {
        if (runLength >= 5) score += 3 + (runLength - 5);
        runColor = line(i);
        runLength = 1;
      }
    }
    if (runLength >= 5) score += 3 + (runLength - 5);
  };

  // Rule N3: finder-like 1011101 pattern with 4 light modules on either
  // side, scanned as an 11-module sliding window.
  const finderPenalty = (line: (i: number) => boolean) => {
    let window = 0;
    for (let i = 0; i < size; i++) {
      window = ((window << 1) & 0x7ff) | (line(i) ? 1 : 0);
      if (i >= 10 && (window === 0x5d0 || window === 0x05d)) score += 40;
    }
  };

  for (let r = 0; r < size; r++) {
    runPenalty((i) => modules[r][i]);
    finderPenalty((i) => modules[r][i]);
  }
  for (let c = 0; c < size; c++) {
    runPenalty((i) => modules[i][c]);
    finderPenalty((i) => modules[i][c]);
  }

  // Rule N2: 2x2 blocks of a single color.
  for (let r = 0; r < size - 1; r++) {
    for (let c = 0; c < size - 1; c++) {
      const v = modules[r][c];
      if (
        v === modules[r][c + 1] &&
        v === modules[r + 1][c] &&
        v === modules[r + 1][c + 1]
      ) {
        score += 3;
      }
    }
  }

  // Rule N4: deviation of the dark-module proportion from 50%, in 5% steps.
  let dark = 0;
  for (const row of modules) for (const m of row) if (m) dark++;
  score += 10 * Math.abs(Math.ceil((dark * 100) / (size * size) / 5) - 10);

  return score;
}

